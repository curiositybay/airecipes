async function seedIngredients() {
  try {
    console.log('Starting ingredient seeding...');

    // Clear existing ingredients
    await prisma.ingredient.deleteMany({});
    console.log('Cleared existing ingredients');

    // Insert ingredients using upsert
    for (const ingredient of ingredients) {
      await prisma.ingredient.upsert({
        where: { name: ingredient.name },
        update: {
          category: ingredient.category,
          isActive: true,
        },
        create: {
          name: ingredient.name,
          category: ingredient.category,
          isActive: true,
        },
      });
    }

    console.log(`✅ Successfully seeded ${ingredients.length} ingredients`);
  } catch (error) {
    console.error('❌ Error seeding ingredients:', error);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

// Run the seeding if this file is executed directly
if (require.main === module) {
  seedIngredients()
    .then(() => {
      console.log('Ingredient seeding completed successfully');
      process.exit(0);
    })
    .catch(error => {
      console.error('Ingredient seeding failed:', error);
      process.exit(1);
    });
}

